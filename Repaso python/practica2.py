# repaso de funciones

def suma(a,b):
    r = a+b
    return print(r)

def div(a,b):
    r = a/b
    return print(r)

def resta(a,b):
    r = a-b
    return print(r)


def mult(a,b):
    r = a*b
    return print(r)

n = int(input("ingresa numero 1: "))
m = int(input("ingresa numero 2: "))


suma(n,m)
resta(n,m)
mult(n,m)
div(n,m)